// ── 都道府県 → 主要ターミナル駅 フォールバックマップ ────────────────
// STATION_OVERRIDE に明記されていない場合、PREF フォールバックは使わない
// 誤変換リスクが高いため、明示的なオーバーライドのみ適用する
const PREF_TERMINAL = {};

// ── 主処理 ─────────────────────────────────────────────────────
let updated = 0;
const log = [];

for (const dest of dests) {
  if (dest.type !== 'destination') continue;
  const bs   = dest.bookingStation;
  if (!bs) continue;   // null はレンタカー確定。変更不要
  if (!dest.requiresCar) continue;  // 自力で行ける → 現状維持

  const pref = dest.prefecture || '';

  // ① 直接オーバーライドテーブル
  if (STATION_OVERRIDE[bs]) {
    const newBs = STATION_OVERRIDE[bs];
    log.push(`[OVERRIDE] ${dest.name}: ${bs} → ${newBs}`);
    dest.bookingStation = newBs;
    updated++;
    continue;
  }

  // ② 既に主要ターミナルならスキップ
  if (MAJOR_TERMINALS.has(bs)) continue;

  // ③ 都道府県フォールバック
  const terminal = PREF_TERMINAL[pref];
  if (terminal) {
    log.push(`[PREF] ${dest.name}(${pref}): ${bs} → ${terminal}`);
    dest.bookingStation = terminal;
    updated++;
  }
}

fs.writeFileSync(DESTS_PATH, JSON.stringify(dests, null, 2), 'utf-8');

console.log(`\n修正件数: ${updated}`);
console.log('\n--- 変更ログ ---');
log.forEach(l => console.log(l));
