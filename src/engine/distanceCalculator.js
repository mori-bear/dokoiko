/** BFS で地方間の最短距離を返す */
function regionDist(from, to) {
  if (!from || !to) return 99;
  if (from === to) return 0;
  const visited = new Set([from]);
  const queue = [[from, 0]];
  while (queue.length > 0) {
    const [cur, d] = queue.shift();
    for (const next of (REGION_ADJ[cur] ?? [])) {
      if (next === to) return d + 1;
      if (!visited.has(next)) {
        visited.add(next);
        queue.push([next, d + 1]);
      }
    }
  }
  return 99;
}

